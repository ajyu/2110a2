/**
 * This is a class that stores a (x,y) coordinate in double precision.
 */
public class PointDouble {
   private double x, y;

   public PointDouble(double x, double y) {
      this.x = x;
      this.y = y;
   }

   public double getX() {
      return x;
   }

   public double getY() {
      return y;
   }
}
